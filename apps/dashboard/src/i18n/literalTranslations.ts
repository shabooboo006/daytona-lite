export function translateLiteralText(value?: string | null): string {
  if (!value || i18n.resolvedLanguage !== 'zh-CN') {
    return value ?? ''
  }

  return zhCNLiterals[value] ?? value
}

export function translateNode(node: React.ReactNode): React.ReactNode {
  if (typeof node === 'string') {
    return translateLiteralText(node)
  }

  if (!node || typeof node === 'number' || typeof node === 'boolean') {
    return node
  }

  if (Array.isArray(node)) {
    return node.map((child, index) => React.createElement(React.Fragment, { key: index }, translateNode(child)))
  }

  if (React.isValidElement(node)) {
    const children = (node.props as { children?: React.ReactNode } | null)?.children
    if (children === undefined) {
      return node
    }

    return React.cloneElement(node, undefined, translateNode(children))
  }

  return node
}
